// 초기화
document.addEventListener('DOMContentLoaded', function() {
    // URL 파라미터 확인
    const urlParams = new URLSearchParams(window.location.search);
    const policyType = urlParams.get('type');
    
    // 특정 정책 타입이 전달된 경우 해당 정책 바로 표시
    if (policyType && policies[policyType]) {
        showPolicyModal(policies[policyType]);
    } else {
        // 일반적인 정책 페이지 표시
        setupEventListeners();
    }
});

// 이벤트 리스너 설정
function setupEventListeners() {
    // 정책 아이템 클릭
    document.querySelectorAll('.policy-item').forEach(item => {
        item.addEventListener('click', handlePolicyClick);
    });
    
    // 모달 닫기 버튼
    const modalClose = document.getElementById('modal-close');
    if (modalClose) {
        modalClose.addEventListener('click', closeModal);
    }
    
    // 모달 외부 클릭
    const modal = document.getElementById('policy-modal');
    if (modal) {
        modal.addEventListener('click', (e) => {
            if (e.target === modal) {
                closeModal();
            }
        });
    }
    
    // ESC 키로 모달 닫기
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && modal.classList.contains('show')) {
            closeModal();
        }
    });
}

// 정책 클릭 처리
function handlePolicyClick(e) {
    const policyType = e.currentTarget.getAttribute('data-policy');
    const policy = policies[policyType];
    
    if (!policy) return;
    
    showPolicyModal(policy);
}

// 정책 모달 표시
function showPolicyModal(policy) {
    const modal = document.getElementById('policy-modal');
    const modalTitle = document.getElementById('modal-title');
    const modalContent = document.getElementById('modal-content');
    
    // 모달 내용 설정
    modalTitle.textContent = policy.title;
    modalContent.innerHTML = policy.content;
    
    // 모달 표시
    modal.classList.add('show');
    document.body.style.overflow = 'hidden';
}

// 모달 닫기
function closeModal() {
    const modal = document.getElementById('policy-modal');
    modal.classList.remove('show');
    document.body.style.overflow = '';
}